<template>
  <div>
    <h1>Posts</h1>
    <Post
      v-for="post in posts"
      :key="post.id"
      :title="post.title"
      :id="post.id" />
  </div>
</template>

<script>
import axios from 'axios'
import Post from '../../components/Posts'

export default {
  components: {
    Post
  },
  data() {
    return {
      posts: [],
    }
  },
  async created() {
    try {
      const res = await axios.get('https://jsonplaceholder.typicode.com/posts')
      this.posts = res.data
    } catch (error) {
      console.error(error)
    }
  },
}
</script>

